#Hallamos la velocidad final de un móvil
velocidad_inicial, aceleración, tiempo, velocidad_final  = 0.0, 0.0, 0.0, 0.0

#Asiganacion de valores
velocidad_inicial= 6.0
aceleración= 5.0
tiempo= 8.0

#Calculo
velocidad_final= (velocidad_inicial+aceleración)*tiempo

#Mostrar Valores
print("velocidad inicial:", velocidad_inicial)
print("aceleracion:", aceleración)
print("tiempo:", tiempo)
print("velocidad final:", velocidad_final)
